/// <summary>
/// Summary description for RandomString
/// </summary>
public class RandomString
{
    private Random random = new Random();

    public string GenerateRandomCode()
    {
        string s = "";
        for (int i = 0; i < 6; i++)
            s = String.Concat(s, this.random.Next(10).ToString());
        return s;
    }
}
